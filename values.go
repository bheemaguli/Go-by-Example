package main

import "fmt"

func main() {
	fmt.Println("go" + "lang") // string concat

	fmt.Println("1+1 = ", 1+1)         // Integer Addition
	fmt.Println("7.0/3.0 = ", 7.0/3.0) // Float division

	// Boolean operation
	fmt.Println(true && false) // And
	fmt.Println(true || false) // Or
	fmt.Println(!true)         // Not
}
